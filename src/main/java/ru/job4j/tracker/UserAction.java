package ru.job4j.tracker;

/**
 * Интерфейс, который описывает все действия системы.
 * Часть методов в нашем коде принимает два параметра, а другая часть один -создадим интерфейс с двумя методами.
 * execute возвращает boolean переменная. Это нужно, чтобы создать действия выхода из программы.
 * Если действие вернуло false, то мы считаем, что пользователь вышел из системы.
 */

public interface UserAction {
    String name();

    boolean execute(Input input, Tracker tracker);
}
